# REVISED: Lazy-load NVM
export NVM_DIR="$HOME/.nvm"
nvm() {
  unset -f nvm node npm npx
  [ -s "$NVM_DIR/nvm.sh" ] && \. "$NVM_DIR/nvm.sh"
  nvm "$@"
}

node() { nvm > /dev/null; node "$@" }
npm() { nvm > /dev/null; npm "$@" }
npx() { nvm > /dev/null; npx "$@" }
